package lessons;

public class Variables {

	public static void main(String[] args) {

		// integers
		int myIntegers1 = 13;
		int myIntegers2 = 22;
		
		// doubles
		double myDouble1 = 13.3;
		
		// booleans
		boolean myBoolean1 = true;
		boolean myBoolean2 = false;
		
		// chars
		char myChar1 = 'a';
		
		// Strings
		String myString1 = "hello";
		String myString2 = "$$$$$";
		
		// Print variables
		System.out.println(myString1);
		
		
		
	}

}
